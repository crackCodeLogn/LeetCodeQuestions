  public int minDominoRotations(int[] tops, int[] bottoms) { // 11ms, beats 25.87%
    boolean top = true, bottom = true;
    boolean topFound = true, bottomFound = true;
    int swapTop = 0, swapBottom = 0;
    int swapTop2 = 0, swapBottom2 = 0;
    int n = tops.length;

    for (int i = 0; i < n; i++) {
      if (tops[i] != tops[0]) top = false;
      if (bottoms[i] != bottoms[0]) bottom = false;
      if (tops[i] != tops[0] && bottoms[i] != tops[0]) topFound = false;
      if (tops[i] != bottoms[0] && bottoms[i] != bottoms[0]) bottomFound = false;

      if (tops[i] != tops[0]) swapTop++;
      if (bottoms[i] != bottoms[0]) swapBottom++;
      if (tops[i] != bottoms[0]) swapTop2++;
      if (bottoms[i] != tops[0]) swapBottom2++;
    }
    if (top || bottom) return 0;
    if (!topFound && !bottomFound) return -1;

    if (topFound && bottomFound) return Math.min(swapTop, swapBottom);
    else if (topFound) return Math.min(swapTop, swapBottom2);
    else return Math.min(swapBottom, swapTop2);
  }
