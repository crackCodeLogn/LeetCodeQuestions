  public int minimumIndex(List<Integer> nums) {
    final int n = nums.size();
    Map<Integer, Integer> count1 = new HashMap<>();
    Map<Integer, Integer> count2 = new HashMap<>();

    for (int num : nums) count2.merge(num, 1, Integer::sum);

    for (int i = 0; i < n; ++i) {
      int freq1 = count1.merge(nums.get(i), 1, Integer::sum);
      int freq2 = count2.merge(nums.get(i), -1, Integer::sum);
      if (freq1 * 2 > i + 1 && freq2 * 2 > n - 1 - i) return i;
    }

    return -1;
  }

  public int minimumIndex_tle(List<Integer> nums) { // tle
    Map<Integer, Integer> map = new HashMap<>();
    PriorityQueue<Integer> maxHeap =
        new PriorityQueue<>((k, v) -> map.get(v).compareTo(map.get(k)));
    List<Integer> ltr = new ArrayList<>(nums.size());
    List<Integer> rtl = new ArrayList<>(nums.size());
    int cnt = 1;
    for (int i = 0; i < nums.size(); i++) {
      int num = nums.get(i);
      map.put(num, map.getOrDefault(num, 0) + 1);
      maxHeap.remove(num);
      maxHeap.add(num);
      Integer key = maxHeap.peek();
      int dominant = (map.get(key) > cnt / 2 || cnt == 1) ? key : -1;
      ltr.add(dominant);
      //      System.out.println(i + " " + key + " " + dominant + " " + map + " " + maxHeap);
      rtl.add(-1);
      cnt++;
    }

    map.clear();
    maxHeap.clear();
    cnt = 1;
    for (int i = nums.size() - 1; i >= 0; i--) {
      int num = nums.get(i);
      map.put(num, map.getOrDefault(num, 0) + 1);
      maxHeap.remove(num);
      maxHeap.add(num);
      Integer key = maxHeap.peek();
      int dominant = (map.get(key) > cnt / 2 || cnt == 1) ? key : -1;
      rtl.set(i, dominant);
      cnt++;
    }

    for (int i = 0; i < nums.size() - 1; i++)
      // if (ltr.get(i) == rtl.get(i + 1) && ltr.get(i) != -1) return i;
      if (ltr.get(i).equals(rtl.get(i + 1)) && ltr.get(i) != -1) return i;

    return -1;
  }
